with open("FS_V2.txt", "r") as f:
    NT = 0
    NF = 0
    current = ""
    for l in f.readlines():
        t = l.strip()
        if t == "True":
            NT += 1
        elif t == "False":
            NF += 1
        elif " s" in t:
            timeWP = float(t[:-2])*40/(NT+NF)
            print(current, t, timeWP)
        else:
            total = NT+NF
            if total != 0:
                print(current, NT/total, total)
            current = t
            NF = 0
            NT = 0
            
